def print_df_info(df, info=False, head=False, describe=False, lines=5):
    print('===========================================================================================================')
    print(df.columns, df.shape)
    if info is True:
        print('***********************************************************************************************************')
        print(df.info(null_counts=True))
    if head is True:
        print('***********************************************************************************************************')
        print(df.head(lines))
    if describe is True:
        print('***********************************************************************************************************')
        print(df.describe())
    print('===========================================================================================================')
